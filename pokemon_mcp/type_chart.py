def single_effectiveness(attacking: str, defending: str) -> float:
    return _CHART.get(attacking.lower(), {}).get(defending.lower(), 1.0)


def effectiveness(attacking: str, defending_types: list[str]) -> float:
    """攻撃タイプ x 防御タイプ(複合可)の総合倍率。例: ice vs [ground, dragon] = 4.0"""
    mult = 1.0
    for d in defending_types:
        mult *= single_effectiveness(attacking, d)
    return mult
